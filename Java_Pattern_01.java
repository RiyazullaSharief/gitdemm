public class Java_Pattern_01 {
	public static void main(String[] args) {
		String str ="JAVA";
		char[] a = str.toCharArray();
		for(int i=0;i<4;i++)
		{
			for(int j=0;j<=i;j++)
			{
				System.out.print(a[j]);
			}
			System.out.println();
		}
	
	}
	
		
	}

	


